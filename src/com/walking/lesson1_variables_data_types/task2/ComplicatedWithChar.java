package com.walking.lesson1_variables_data_types.task2;

// Вывести ваше имя посимвольно, где каждая буква — на новой строке, не используя System.out.println().
// Реализуется с использованием char.

public class ComplicatedWithChar {
    public static void main(String[] args) {
        char singleChar = 'J';
        System.out.print(singleChar);
        System.out.print("\n");

        singleChar = 'o';
        System.out.print(singleChar);
        System.out.print("\n");

        singleChar = 'h';
        System.out.print(singleChar);
        System.out.print("\n");

        singleChar = 'n';
        System.out.print(singleChar);
        System.out.print("\n");
    }
}
