package com.walking.lesson1_variables_data_types.task2;

// Используя
// Одну переменную типа char.
// Вывести свое имя в консоль.

public class SingleCharVariables {
    public static void main(String[] args) {
        char singleChar = 'J';
        System.out.print(singleChar);

        singleChar = 'o';
        System.out.print(singleChar);

        singleChar = 'h';
        System.out.print(singleChar);

        singleChar = 'n';
        System.out.print(singleChar);
    }
}
